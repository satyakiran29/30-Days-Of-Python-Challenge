#9. Write a program using multiple If conditions to check greater than/less than/Equal to condition among 2 values.Input: a=10, b=15,Output: a less than b
a=int(input("enter a first number"))
b=int(input("enter a second number"))
if a==b:
     print("equal")
elif a>b:
     print("a greater then b")  
elif a<b:
     print(" a less then b ")       
else:
     print("error")   